public class Switchandternaryoperator {
    public static void main(String[] args) {

        // this is we can use multiple  data types : Char,String,
        // double it wont be take it

        int n = 1;
        switch (n) {
            case 1:

                System.out.println("one");
                break;
            case 2:
                System.out.println("two");
                break;
            default:
                System.out.println("no match");
        }

    }
}

